            data: daily.quarantined.data
          },
          {
            name: this.$t("covid_stages.confirmed"),
            data: daily.confirmed.data
          },
          {
            name: this.$t("covid_stages.hospitalized"),
            data: daily.hospitalized.data
          },
          {
            name: this.$t("covid_stages.hospitalized_icu"),
