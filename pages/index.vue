          categories: daily.confirmed.labels,
          title: {
            text: this.$t("calendar.days")
          }
        };

        const series = [
          {
            name: this.$t("covid_stages.quarantined"),
            data: daily.quarantined.data
          },
          {
            name: this.$t("covid_stages.confirmed"),
            data: daily.confirmed.data
